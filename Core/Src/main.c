void get_signal(void){
	if(flag_get_signal){
		if(send == SIM_OK){
			uint8_t size = strlen(rx_buffer);
			if(size > 16 && size <= 28){
				signal = 0;
				for(uint8_t i=0; i<strlen(rx_buffer); i++){
					if(rx_buffer[i] == ',') return;
					if(rx_buffer[i] >= '0' && rx_buffer[i] <= '9'){
						signal = signal * 10 + (rx_buffer[i] - '0');
					}
				}

			}
		}else{
			signal = 0;
		}
		flag_get_signal = 0;
	}
}

void check_button_manual_press(void){
	if(adc_value[0] >= 250 && adc_value[0] <= 350){
		warning_flag = 1;
		HAL_UART_Transmit(&huart2, (uint8_t*)"1", 1, 1000);
		HAL_NVIC_DisableIRQ(USART2_IRQn);
	}
	if(adc_value[0] >= 1950 && adc_value[0] <= 2050){
		warning_flag = 0;
		HAL_NVIC_EnableIRQ(USART2_IRQn);
		HAL_UART_Transmit(&huart2, (uint8_t*)"0", 1, 1000);
		HAL_UART_Receive_IT(&huart2, &zigbee_data_rx, 1);
	}
}


